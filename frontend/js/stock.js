// ─── Auth ─────────────────────────────────────────────────────────────────────

async function login(username, password) {
    const errorEl = document.getElementById('login-error');
    if (errorEl) errorEl.style.display = 'none';

    try {
        const res = await fetch('/api/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ username, password })
        });
        const data = await res.json();
        if (!res.ok || !data.token) {
            throw new Error(data.error || 'Credenciales inválidas');
        }

        const payload = JSON.parse(atob(data.token.split('.')[1]));
        if (payload.role !== 'operador_stock' && payload.role !== 'admin') {
            throw new Error('Acceso denegado. Solo operadores de stock.');
        }

        localStorage.setItem('stock_token', data.token);
        currentToken = data.token;
        currentUser = payload;
        showDashboard();
    } catch (err) {
        if (errorEl) { errorEl.textContent = err.message; errorEl.style.display = 'block'; }
    }
}

function logout() {
    localStorage.removeItem('stock_token');
    currentToken = '';
    currentUser = null;
    location.reload();
}

function checkAuth() {
    const token = localStorage.getItem('stock_token');
    if (!token) return false;
    try {
        const payload = JSON.parse(atob(token.split('.')[1]));
        const exp = payload.exp * 1000;
        if (Date.now() >= exp) {
            localStorage.removeItem('stock_token');
            return false;
        }
        if (payload.role !== 'operador_stock' && payload.role !== 'admin') {
            localStorage.removeItem('stock_token');
            return false;
        }
        currentToken = token;
        currentUser = payload;
        return true;
    } catch {
        localStorage.removeItem('stock_token');
        return false;
    }
}

